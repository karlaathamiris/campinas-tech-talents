package primitivos;

public class DadosPrimitivos {

	public static void main(String[] args) {
		//Definindo explicitamente que o valor é float
				float a = 10.99f;
				
				//Definindo explicitamente que o valor é double
				double b = 10.3D;
				
				//Atribuindo um valor inteiro para um tipo double
				double c = 5;
				
				//Atribuindo um valor double, por padrão todos os números com casas decimais são do tipo double.
				double d = 7.2;
				
				
				System.out.println(a);
				System.out.println(b);
				System.out.println(c);
				System.out.println(d);


	}

}
